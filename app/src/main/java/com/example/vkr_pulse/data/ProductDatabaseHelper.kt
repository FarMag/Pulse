    override fun onUpgrade(db: SQLiteDatabase?, oldVersion: Int, newVersion: Int) {
        db?.let {
            it.execSQL("DROP TABLE IF EXISTS products")
            onCreate(it)
        }
    }

    fun searchProducts(query: String): List<FoodItem> {
        val db = readableDatabase
        val cursor = db.rawQuery(
            "SELECT * FROM products WHERE product_name LIKE ?", arrayOf("%$query%")
        )

        val results = mutableListOf<FoodItem>()
        while (cursor.moveToNext()) {
            results.add(
                FoodItem(
                    id = cursor.getInt(0),
                    name = cursor.getString(1),
                    calories = cursor.getInt(2),
                    protein = cursor.getFloat(3),
                    fats = cursor.getFloat(4),
                    carbs = cursor.getFloat(5)
                )
            )
        }
        cursor.close()
        return results
    }
